class Product {
  String name;
  double price;
  String store;
  String imageURL;
  String brand;
  String distance;

  Product(String name, double price, String store, String imageURL, String brand, String distance){
    this.name = name;
    this.price = price;
    this.store = store;
    this.imageURL = imageURL;
    this.brand = brand;
    this.distance = distance;
  }
}